<?php

function suma($num1, $num2){
    return $num1 + $num2;
}

function resta ($num1, $num2){
    return $num1 - $num2;
}

function mutliplicacion ($num1 , $num2){
    return $num1 * $num2;
}

function division ($num1, $num2){
    return $num1 / $num2;
}

function modulo ($num1, $num2){
    return $num1 % $num2;
}

?>